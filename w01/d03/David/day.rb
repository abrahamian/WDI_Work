puts "Please enter the day of the week"
day = gets.chomp.capitalize

case day
when "Monday"
  puts "#{day} sucks....drink some coffee"
when "Tuesday"
  puts "#{day}'s the worst....get drunk at work!"
when "Wednesday"
  puts "#{day} is hump day....smoke some Camels!!!"
when "Thursday"
  puts "#{day} is awesome, smoke some crack!"
when "Friday"
  puts "#{day} is the best day to kill your boss!!"
end