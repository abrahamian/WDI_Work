puts "What day of the week is it?"
day_of_week = gets.chomp.downcase 

case day_of_week 
when "monday"
  puts "Sorry, man."
when "tuesday"
  puts "Day two!"
when "wednesday"
  puts "Hump day!"
when "thursday"
  puts "Almost there!"
when "friday"
  puts "TGIF!"
when "saturday"
  puts "Woot! Weekend!"
when "sunday"
  puts "Afternoon loungin'."
end 

