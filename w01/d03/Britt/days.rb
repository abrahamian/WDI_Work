
puts "What day of the week is it today?"
today = gets.chomp.downcase

case today
when "monday"
	puts "Oh no... I hope you don't get a case of the Mondays!"
when "tuesday"
	puts "You made it through Monday! Today will be a breeze in comparison!"
when "wednesday"
	puts "Happy Hump Day! You're half way there...!"
when "thursday"
	puts "Keep pushing through this week. You're doing great!"
when "friday"
	puts "YOU MADE IT!!! TGIF!!!!!aldufkhsdoincgkusjdhm"
else
	puts "I don't think that's a work day... so what're you doing at work?!"
end