puts "What day of the week is it?"
day = gets.chomp.downcase
case day
when "monday"
	puts "Somebody's got a case of the monday's!"
when "tuesday"
	puts "It's the least exciting day of the week"
when "wednesday"
	puts "Guess what day it is... It's hump day!"
when "thursday"
	puts "one more day til Friday!"
when "friday"
	puts "It's Friday, FREEDOM!"
when "saturday"
	puts "It's Saturday, live it up!"
when "sunday"
	puts "It's sunday, relax because you start over tomorrow!"
end